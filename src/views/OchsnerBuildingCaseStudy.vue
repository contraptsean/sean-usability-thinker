            <article>
            <p class="lead">When I joined Ochsner Health as a Digital Platforms Specialist & UX Researcher, I stepped into a place where there had been no dedicated UX or research function previously, and many of the foundational practices required for a healthy, mature UX program simply didn’t exist.</p>

<p>My focus was not just on improving individual interfaces, but on establishing the systems, metrics, and project management practices needed to make experience design not only measurable, but sustainable, and strategically relevant across the institution.</p>


<h2>Problem</h2>

<p>Before I joined, success was defined almost entirely at the institutional and marketing level, with limited visibility into how digital experiences impacted patient behavior, satisfaction, or conversion. There was no consistent way to understand the voice of the customer, evaluate experience quality, or connect UX improvements to ROI.</p>

<p>Without change, Ochsner risked losing users due to poor digital experiences in an industry where confidence and trust directly influence patient choice. The lack of UX practices also meant teams were operating in silos, relying on assumptions rather than evidence when making experience decisions.</p>




<h2>Strategy</h2>

<p>Rather than starting with isolated design fixes, I took a foundational, systems-level approach. My strategy was to start by defining what “success” meant for CX and UX at Ochsner. Then to build the research and measurement infrastructure to support that definition, while creating shared spaces for cross-functional alignment and decision-making.</p>

<p>The goal was to move UX from a reactive, ad hoc function to a strategic one. One that could continuously improve patient experiences over time, and use that to influence executive priorities, and demonstrate ROI. The following projects supported that vision:</p>

<h2>Projects & Impact</h2>
<p>A core part of my work was defining success through an analytics framework that tied CX metrics, such as task success and satisfaction, to ROI and institutional outcomes like appointment conversions. This framework influenced executive workgroups, including emergent access initiatives, and helped establish a Voice of the Customer as a formal yearly goal. By tracking improvements in task success and satisfaction alongside conversion data, I was able to demonstrate the tangible business impact of UX improvements.</p>

<p>To better understand patient needs, I created user personas and journey maps focused on the highest impact experiences, particularly the Find a Doctor journey. This work uncovered critical issues, such as difficulty comparing providers and losing progress when navigating backward in search, which were later validated through qualitative research. The resulting insights informed design changes to search and provider pages, which led to noticeable increases in task success, satisfaction, and appointment conversions.</p>

<p>I also established research operations that enabled us to recruit and manage a participant panel, allowing us to conduct regular usability testing without relying on external vendors. This not only reduced research costs, but significantly increased the speed and frequency of user feedback, embedding research as a repeatable institutional capability.</p>

<p>To measure progress over time, I implemented touchpoint surveys on a regular cadence, capturing both quantitative and qualitative feedback. These surveys created longitudinal visibility into experience improvements and supported the collection of much needed targeted qualitative feedback.</p>

<p>Finally, I launched and facilitated a monthly UX workgroup that brought together stakeholders from marketing, web development, web content, and communications. This forum created a shared space for decision-making, clarified ownership across design and implementation, and reduced siloed UX efforts. The workgroup remains active today and continues to shape UX direction at Ochsner.</p>


<h2>Conclusion</h2>
<p>Through this work, I helped shift UX at Ochsner Health from an undefined, ad hoc practice to a measurable, strategic, and sustainable capability. By connecting digital experience quality to institutional outcomes, establishing durable research and measurement systems, and fostering cross-functional alignment, I demonstrated how service design and UX strategy can meaningfully influence both patient experience and organizational success in a complex healthcare environment.</p>


