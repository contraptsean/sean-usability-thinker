<h2>Introduction</h2>

<p>I go to a curl specialist to style and trim my hair.</p>

<p>My stylist’s studio was running their entire business through separate instagrams, and one day expressed frustration about: </p>
<ol>
<li>having to answer the same questions over and over (mostly pertaining to their location) and </li>
<li>having to run their business through the instagram platform</li>
</ol>

<p>I offered to try to help solve some of these issues, and we have been working on the site collaboratively since October. I would say the site is ready (and ready for testing and iteration) but not launched yet. I believe I am waiting on some pictures to feature a gallery of work on the artist pages.</p>

<p>My role, was to design a usable hair education and salon space website that matched with beauty site conventions, and the context of use for common hair appointment making behaviors, as well as helping my stakeholders, Arista and Jordan, to generate web-friendly content for the site.</p>

<h2>Exploration</h2>

<p>This wasn’t just making a normal hair salon website, and was challenging for the following reasons:</p>
<ul>
<li>This is a *Hair Education* studio, so I wanted to highlight these parts of the business and help those artists</li>
<li>All the artists have different services and payment workflows, and some had their own sites that they want to manage business through</li>
<li>My Stakeholders had no content for this type of medium</li>
</ul>
<h2>Process</h2>

<p>As usual, I started with seeking to understand my "users" (I try not to use that word) or audience, and how they might utilize the website, which this time was very different than the user set of my job at my </p>institution.

<p>Ubiquitously I found that potential clients wanted to utilize the site on their phone, and while I should build out a beautiful desktop experience, I needed to focus on the mobile performance, particularly since my stakeholders would later request a video header.</p>

<p>I did an environmental scan, and went over some other similar websites with my stakeholders, and derived what they might want to see in a website for their business.</p>

<p>I put together some wireframes, like <a href="https://www.figma.com/file/K3qk9XeTbR1D6q7QZvRVBg/Morph-artist-page-1?node-id=0%3A1">these for the artist pages</a>, and created a <a href="https://docs.google.com/document/d/1-pYOpLBz51QlXkzcMwBrrYj7qrO3_eM-rdryIBTXULo/edit?usp=sharing">content document</a> to help focus the content on the types of questions that the owners were receiving as well as leave some space for each artist to arrange content and services in a way that best worked for them.</p>

<h2> Challenges</h2>
<ul>
<li>This wasn’t a simple brochure site, it required making a collective of artists seem seamless and unified, and the stakeholders wanted some fun yet complex visual elements.</li>
<li>Getting content together has taken a long time, and I’m still waiting on some. Usually in working with clients or stakeholders, I try to help with writing for the web, and making the site as readable and learnable as possible, its always a challenge to get the right type of content from stakeholders, and this type of content (in the beauty/hair industry) was a different and challenging type of writing consideration.</li>
</ul>

<h2>Takeaways</h2>

<p>I would love to say that they have signed a lot of new clients, or receive exactly the input they expect to receive on their calendaring systems, and it’s all exactly right, but we haven’t launched the site quite yet.</p>

<p>This was the first time I had used Figma, and it was a moderately easy tool to use. I will probably use it or tools like it in the future rather than MS Paint.</p>

