            <article>
            <p class="lead">One of the biggest challenges I tackled was figuring out why people did, or did not participate in the research program I was working on. The goal was to identify motivations and barriers to participation. Only one problem, I wasn’t allowed to speak to actual participants or observe an intake session. Instead, I had to rely on insights from hospital staff and research participation.</p>
            <p>To research in this callenging context, I developed a qualitative technique that I called <strong>New Orleans Bingo</strong>.</p>

<h2>Research & Approach</h2>

<p>I visited several hospitals in our partner network, talking to staff who engaged with potential participants daily. To structure my investigation, I designed an impromptu research method I called New Orleans Bingo.</p>

<p><strong>I started with a list of assumptions</strong> and hypotheses about why people might join or avoid the program. <strong>Every time I heard an assumption confirmed, I highlighted it</strong>. When new insights emerged, I added them to a separate list. This approach allowed me to validate patterns and uncover fresh perspectives on participant behavior.</p>
    
<img src="@/assets/no-bingo-1.png" class="w-100" />

<h2>Key Observations</h2>

<p>From my conversations, it became clear that frontline staff had deep, experience-based knowledge about participant concerns. They were engaging with our participants every day and hearing their doubts and concerns firsthand.</p>

<p>To take this a step further, I ran a simple but effective exercise. I gave staff a magic wand and asked:</p>
<blockquote><p>If you could change anything about the intake, onboarding, or participation process, what would it be?</p></blockquote>
<p>Their responses helped shape a list of potential improvements that we could explore further.</p>


<h2>Findings & Insights</h2>

<p>At the end of my research, I walked away with a strong list of validated and semi-validated reasons why people participated, or didn’t. The three biggest barriers to participation were:</p>
<ul>
    <li>Lack of trust in the government, especially in medical research.</li>

    <li>Low perceived value in what they received in return for participation.</li>

    <li>A mismatch between what participants found valuable and what the program actually offered.</li>
</ul>


<p>
    These insights became the beginnings of a clear roadmap for improving adoption and retention.

</p>
<h2>
    Next Steps: Building a Better Program
</h2>

<p>
    Understanding these barriers pushed me to think differently about how we measured success. I began developing new metrics to investigate trust and perceived value, focusing on ways to better connect with underrepresented populations in biomedical research.
</p>
<p>
This project reinforced the power of qualitative research, even when direct participant access isn’t available. By leveraging staff insights and designing creative research methods, I uncovered actionable ways to improve program adoption and retention.
