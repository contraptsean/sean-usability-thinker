            <h1>Inventing a New Research Method: New Orleans Bingo</h1>
            </header>
            <article>
            <p class="lead">One of the biggest challenges I tackled was figuring out why people did—or didn’t—participate in the research program I was working on. The goal was to identify motivations and barriers to participation. Only one problem, I wasn’t allowed to speak to actual participants or observe an intake session. Instead, I had to rely on insights from hospital staff and research participation.</p>

<h2>Research & Approach</h2>

<p>I visited several hospitals in our partner network, talking to staff who engaged with potential participants daily. To structure my investigation, I designed an impromptu research method I called <b>New Orleans Bingo</b>.</p>

<p>I started with a list of assumptions and hypotheses about why people might join or avoid the program. Every time I heard an assumption confirmed, I highlighted it. When new insights emerged, I added them to a separate list. This approach allowed me to validate patterns and uncover fresh perspectives on participant behavior.</p>
    

<h2>Key Observations</h2>

<p>From my conversations, it became clear that frontline staff had deep, experience-based knowledge about participant concerns. They weren’t just following a script, they were engaging with real people and hearing their doubts and concerns firsthand.</p>

<p>To take this a step further, I ran a simple but effective exercise. I gave staff a magic wand and asked:</p>
<p><b><i>If you could change anything about the intake, onboarding, or participation process, what would it be?</i></b></p>
<p>Their responses helped shape a list of potential improvements that we could explore further.</p>


<h2>Findings & Insights</h2>

<p>At the end of my research, I walked away with a strong list of validated and semi-validated reasons why people participated—or didn’t. The three biggest barriers to participation were:</p>
<ul>
    <li>Lack of trust in the government, especially in medical research.</li>

    <li>Low perceived value in what they received in return for participation.</li>

    <li>A mismatch between what participants found valuable and what the program actually offered.</li>
</ul>


<p>
    These insights weren’t just theoretical—they were a clear roadmap for improving adoption and retention.

</p>
