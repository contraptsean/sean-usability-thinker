<h2>Introduction</h2>

<p>As the Senior Usability Strategist at the All of Us research program, I was tasked broadly with studying participant satisfaction. In this case study, I outline the methodology and findings in creating this study to understand participant satisfaction, and how it might apply to program goals. In addition it will discuss the resulting implementation of changes aimed at improving retention goals for the program.</p>

<h2>Methodology</h2>

 <p>To assess participant satisfaction, I developed a survey consisting of three key questions: one Likert scale question regarding satisfaction, another about motivation, and NPS scale question on likelihood to recommend the program. Additionally, an open-text question was included with the likelihood to recommend question to gather qualitative data points. I conducted analysis using R, comparing responses across many facets including: demographics, activity completion, and whether participants received genetic return of results.</p>

<h2>Findings</h2>


<p>The analysis revealed that satisfaction levels did not significantly differ across participant segments or program duration. However, participants who received genetic results expressed notably higher satisfaction levels. Qualitative analysis of open-text responses highlighted a recurring theme of interest in DNA and genetic results among those with low satisfaction or likelihood to recommend.</p>

<p>Furthermore, the survey unveiled a difference in participant motivation than others had hypothesize. While previously assumed that monetary incentives and genetic results were primary motivators, the data indicated that the desire to contribute to scientific advancement emerged as the predominant motivation.</p>


<h2>Recommendations and Implementation</h2>
<p>In the program I was tasked with designing returns for “reassessment” or re-taking core data measurements from participants. Utilizing these insights, I proposed leveraging this motivation to enhance participant retention. </p>

<p>I introduced and designed the concept of the "Return of Research Story," a personalized narrative illustrating the impact of participants' contributions to scientific research. This “story” not only showcases the research conducted using participants' data but also serves as a catalyst for continued participation by highlighting available activities and ongoing scientific endeavors.</p>
