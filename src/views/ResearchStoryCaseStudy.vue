<h2>Introduction</h2>

<p>Utilizing findings from a small study I designed around participant satisfaction and motivation, I identified an opportunity, and designed a deliverable to take advantage of that opportunity  which resulted in improving retention goals for the program.</p>
<h2>The Opportunity</h2>

<p>The research program had a commitment and unique opportunity to return valuable things during the reassessment experience. However there was not a good opportunity to return information from the surveys they were taking in reassessment, since they already had to take those surveys.</p>

<p>The results of the survey I had previously designed indicated that Contribution to Science was the top reason for participants to join the program, rather than the monetary incentive. I identified an opportunity to create something valuable that matched with that top motivation where other programmatic returns and valuable deliverables had not.</p>

<p>I introduced and designed the concept of the "Return of Research Story," a personalized narrative illustrating the impact of participants' contributions to scientific research. This “story” not only showcases the research conducted using participants' data but also serves as a catalyst for continued participation by highlighting available activities and ongoing scientific endeavors.</p>
<img src="@/assets/research-story.png" class="w-100" />


<h2>Results and Conclusion</h2>

<p>The implementation of the "Return of Research Story" yielded promising results, not only testing well in moderated sessions, but also fostering increased participant engagement metrics in limited A/B testing with reassessment activities. By aligning program messaging with participants' intrinsic motivation to contribute to science, I essentially enhanced the overall participant experience.</p>

<p>This deliverable highlights why it's key to align what we do based on what our usability data tells us. When we put our users first and make the most of the insights our studies give us, we can create engaging and usable experiences that meet our business goals</p>


